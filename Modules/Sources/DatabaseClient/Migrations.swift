/// Tokenizer wrapping unicode61 with additional properties.
///
/// unicode61 options:
/// - Add ' (apostrophe) as a token character, ensures that "o'hare" is tokenized as-is instead of two words
///
/// Custom tokenization:
/// - Normalize words containing ' (apostrophe) in order to match the term "ohare" with "o'hare"
final class SearchNormalizingTokenizer: FTS5WrapperTokenizer {

    static let name = "search_normalizer"

    let wrappedTokenizer: any FTS5Tokenizer

    init(db: GRDB.Database, arguments: [String]) throws {
        wrappedTokenizer = try db.makeTokenizer(.unicode61(tokenCharacters: ["'"]))
    }

    func accept(
        token: String,
        flags: GRDB.FTS5TokenFlags,
        for tokenization: GRDB.FTS5Tokenization,
        tokenCallback: (String, GRDB.FTS5TokenFlags) throws -> Void
    ) throws {

        let lowercasedToken = token.lowercased()
        let normalizedToken = lowercasedToken.replacingOccurrences(of: "'", with: "")

        if !normalizedToken.isEmpty {
            try tokenCallback(normalizedToken, flags)
        }
    }
}
